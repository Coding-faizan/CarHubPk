class Ad {
  constructor({
    images,
    title,
    makerId,
    modelId,
    registeredYear,
    variant,
    transmission,
    location,
    price,
    description,
    sellerId,
    date,
  }) {
    this.images = images;
    this.title = title;
    this.makerId = makerId;
    this.modelId = modelId;
    this.registeredYear = registeredYear;
    this.variant = variant;
    this.transmission = transmission;
    this.location = location;
    this.price = price;
    this.description = description;
    this.sellerId = sellerId;
    this.date = date;
  }
}
